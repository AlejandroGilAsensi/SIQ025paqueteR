#' Multiplicación entre dos números
#'
#' Esta función devuelve la multiplicación entre dos números.
#'
#' @param a Primer número
#' @param b Segundo número
#'
#' @return El producto entre a y b
#' @export
#'
#' @examples
#' mult(4,2.3)
#' mult(0,3.7)
#' mult(-4/3,98.123)
#' mult(pi,3)
#'
#' @seealso \link{div}, \link{suma}, \link{resta}.
#'
mult = function(a,b){
  return(a * b)
}
